var dunMapFormat = {
	name: "DUN map format",
	extension: "dun",

	write: function(map, fileName) {
		var m = {
			width: map.width,
			height: map.height,
			layers: []
		};

		for (var i = 0; i < map.layerCount; i++) {
			var layer = map.layerAt(i);
			if (layer.isTileLayer) {
				var rows = [];
				for (var y = 0; y < layer.height; y++) {
					var row = [];
					for (var x = 0; x < layer.width; x++)
						row.push(layer.cellAt(x, y).tileId);
					rows.push(row);
				}
				m.layers.push(rows);
			}
		}

		// TODO: lookup dungeon type from tileset image name? For now, we are generating Tristram.
		var tileIDFromTile = townTileIDFromTile;

		// Convert dpiece IDs to tile IDs.
		var dpeiceIDs = m.layers[0]; // use layer 0 as dungeon piece ID layer.
		var tileIDs = [];
		for (var y = 0; y < m.height; y += 2) {
			var row = [];
			for (var x = 0; x < m.width; x += 2) {
				var top = dpeiceIDs[x][y] + 1;
				//var right = dpeiceIDs[x+1][y] + 1;
				//var left = dpeiceIDs[x][y+1] + 1;
				var right = dpeiceIDs[x][y+1] + 1; // x direction in Tiled is y in Diablo?
				var left = dpeiceIDs[x+1][y] + 1;  // x direction in Tiled is y in Diablo?
				var bottom = dpeiceIDs[x+1][y+1] + 1;
				var tile = `{top=${top}, right=${right}, left=${left}, bottom=${bottom}}`;
				if (!(tile in tileIDFromTile)) {
					console.log(`tile ${tile} NOT present, are you using a custom tileset?`);
					continue;
				}
				var tileID = tileIDFromTile[tile];
				row.push(tileID);
			}
			tileIDs.push(row);
		}

		var dunWidth = m.width/2;
		var dunHeight = m.height/2;

		// DUN format:
		//    width:   uint16
		//    height:  uint16
		//    tileIDs: [width][height]uint16
		const buffer = new ArrayBuffer(2 * (1 + 1 + dunWidth * dunHeight));

		const view = new DataView(buffer);
		view.setInt16(0, dunWidth, true); // littleEndian=true
		view.setInt16(2, dunHeight, true);
		var i = 0;
		for (var y = 0; y < dunHeight; y++) {
			for (var x = 0; x < dunWidth; x++) {
				var tileID = tileIDs[x][y];
				// 1 for width, 1 for height, and i for tile IDs. each is 2 bytes in size.
				var offset = 2*(1 + 1 + i);
				view.setInt16(offset, tileID, true);
				i++;
			}
		}

		var file = new BinaryFile(fileName, BinaryFile.WriteOnly);
		file.write(buffer);
		file.commit();
	},
}

tiled.registerMapFormat("dun", dunMapFormat)

